package usu.perpustakaan.buku.data;

import usu.perpustakaan.buku.data.template.BookGuest;

/**
 *
 * @author usu
 */
public class DefaultBookGuest implements BookGuest {

    private static final long serialVersionUID = -1;
    private Boolean borrow;
    private int totalBorrow;
    private String id,  publisher,  author,  title,  kind;

    public int getTotalBorrow() {
        return totalBorrow;
    }

    public void setTotalBorrow(int totalBorrow) {
        this.totalBorrow = totalBorrow;
    }

    public DefaultBookGuest() {
    }

    public Boolean isBorrow() {
        return borrow;
    }

    public void setBorrow(Boolean borrow) {
        this.borrow = borrow;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getKind() {
        return kind;
    }

    public void setKind(String kind) {
        this.kind = kind;
    }

    public String getPublisher() {
        return publisher;
    }

    public void setPublisher(String publisher) {
        this.publisher = publisher;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
